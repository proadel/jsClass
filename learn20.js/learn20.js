      /* - A JavaScript function is a block of code designed to 
         perform a particular task. A JavaScript function is 
         executed when "something" invokes it (calls it).
         - A JavaScript function is defined with the function keyword, 
           followed by a name, followed by parentheses ().
         - Function names can contain letters, digits, underscores, 
           and dollar signs (same rules as variables).
         - Function parameters are listed inside the parentheses () in 
           the function definition.
         - Function arguments are the values received by the function 
           when it is invoked.
         - Inside the function, the arguments (the parameters) behave 
           as local variables.
         # Function Invocation
           The code inside the function will execute when "something" 
            invokes (calls) the function:
           - When an event occurs (when a user clicks a button)
           - When it is invoked (called) from JavaScript code
           - Automatically (self invoked)
           - The parentheses may include parameter names separated 
              by commas:
                 (parameter1, parameter2, ...)
           - The code to be executed, by the function, 
             is placed inside curly brackets: {}
      */
